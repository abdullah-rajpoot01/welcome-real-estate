// This creates a union type: "Phone" | "Mail" | "User" | "Home"
type IconName = keyof typeof ICONS;

const isValidIconName = (name: string): name is IconName => {
  return name in ICONS;
};

type IconRendererProps = {
  name: string; // comes from JSON
} & React.ComponentProps<LucideIcon>;

export const IconRenderer = ({ name, ...props }: IconRendererProps) => {
  const IconComponent = isValidIconName(name) ? ICONS[name] : HelpCircle; // fallback

  return <IconComponent {...props} />;
};
